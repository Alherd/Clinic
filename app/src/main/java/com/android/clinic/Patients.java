package com.android.clinic;

public class Patients {
    private String mPatientID;
    private String mPatientLogin;
    private String mPatientPassword;
    private String mPatientFNAME;
    private String mPatientLNAME;
    private String mPatientPNAME;
    private String mPatientBirth;
    private String mPatientAddress;
    private String mPatientEmail;

    public String getPatientID() {
        return mPatientID;
    }

    public void setPatientID(String patientID) {
        mPatientID = patientID;
    }

    public String getPatientLogin() {
        return mPatientLogin;
    }

    public void setPatientLogin(String patientLogin) {
        mPatientLogin = patientLogin;
    }

    public String getPatientPassword() {
        return mPatientPassword;
    }

    public void setPatientPassword(String patientPassword) {
        mPatientPassword = patientPassword;
    }

    public String getPatientFNAME() {
        return mPatientFNAME;
    }

    public void setPatientFNAME(String patientFNAME) {
        mPatientFNAME = patientFNAME;
    }

    public String getPatientLNAME() {
        return mPatientLNAME;
    }

    public void setPatientLNAME(String patientLNAME) {
        mPatientLNAME = patientLNAME;
    }

    public String getPatientPNAME() {
        return mPatientPNAME;
    }

    public void setPatientPNAME(String patientPNAME) {
        mPatientPNAME = patientPNAME;
    }

    public String getPatientBirth() {
        return mPatientBirth;
    }

    public void setPatientBirth(String patientBirth) {
        mPatientBirth = patientBirth;
    }

    public String getPatientAddress() {
        return mPatientAddress;
    }

    public void setPatientAddress(String patientAddress) {
        mPatientAddress = patientAddress;
    }

    public String getPatientEmail() {
        return mPatientEmail;
    }

    public void setPatientEmail(String patientEmail) {
        mPatientEmail = patientEmail;
    }

}
